import Foundation
import UIKit
extension UIFont {
    static func fontMavenProRegular(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "MavenPro-Regular", size: size)
    }
    static func fontMavenProBold(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "MavenPro-Bold", size: size)
    }
    static func fontMavenProMedium(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "MavenPro-Medium", size: size)
    }
    static func fontSkranjiMedium(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "Skranji", size: size)
    }
    static func fontSkranjiBold(_ size: CGFloat) -> UIFont? {
        return UIFont(name: "Skranji-Bold", size: size)
    }
    
 }
